/**
 * Get year from project code
 */
function getYearFromCode(code: string): number {
    const parts = code.split('-');
    if (parts.length >= 2) {
        const yearPart = parts[1];
        if (yearPart.length === 2) {
            return 2000 + parseInt(yearPart);
        }
    }
    return 2024; // default
}

/**
 * Get department based on project type
 */
function getDepartment(name: string): Department {
    const nameLower = name.toLowerCase();
    if (nameLower.includes('instalacion') || nameLower.includes('clima') || nameLower.includes('hvac') || nameLower.includes('aire')) {
        return Department.OTHER;
    }
    if (nameLower.includes('electrical') || nameLower.includes('se ') || nameLower.includes('set ') || nameLower.includes('subestacion') || nameLower.includes('lat ') || nameLower.includes('linea') || nameLower.includes('mt ') || nameLower.includes('kv')) {
        return Department.ELECTRICAL;
    }
    if (nameLower.includes('i&c') || nameLower.includes('control') || nameLower.includes('p&id')) {
        return Department.INSTRUMENTATION;
    }
    if (nameLower.includes('pv ') || nameLower.includes('pfv ') || nameLower.includes('solar') || nameLower.includes('fotovoltaic')) {
        return Department.ELECTRICAL;
    }
    return Department.CIVIL_DESIGN;
}

